# Function for Addition 

num1=int(input("Enter first number: "))
num2=int(input("Enter second number: "))

def add(num1,num2):
    return num1+num2
print(add(num1,num2))


#Function for Subtraction 
def sub(num1,num2):
    return num1-num2
print(sub(num1,num2))


#function for Multiply
def multiply(num1,num2):
    return num1*num2
print(multiply(num1,num2))